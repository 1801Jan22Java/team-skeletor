package com.revature.beans;

public class MyResponseMessage {
	private String msg;

	public MyResponseMessage() {
		super();
	}

	public MyResponseMessage(String msg) {
		super();
		this.msg = msg;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

}
